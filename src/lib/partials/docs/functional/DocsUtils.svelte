    <h2>Utils</h2>
    <p class="lead">
        This collection is currently being curated. We tend to add features abstract enough for 
        project-independent use into this folder. Currently, the following tools are shipped: 
    </p>
    <h3>autoFillStore</h3>
    <p>A hook to fill the store with external data on subscription.</p>
    <h3>dayjsHelper</h3>
    <p>A wrapper around dayjs initialization to cope with date objects from <a href="https://lenkrad.neoan3.rocks/">Lenkrad date objects</a></p>
    <h3>fileSizeConverter</h3>
    <p>Translates bytes into a human-readable format.</p>
    <h3>sleep</h3>
    <p>General purpose delay function.</p>
    <h3>viewportObserver</h3>
    <p>Svelte action to watch an elements visibility in the viewport (based on IntersectionObserver).</p>
